package com.ecommerce.cn.bean;

public class User {
	//Fields
	private String name;
	private String password;

	//Methods
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
}